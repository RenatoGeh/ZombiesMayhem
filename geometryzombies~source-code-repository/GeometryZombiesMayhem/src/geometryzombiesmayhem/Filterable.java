package geometryzombiesmayhem;

import geometryzombiesmayhem.GameSettings.Graphics.Filter;

/**
 * <p>Represents a filterable image, that is, capable of image filtering.</p>
 * 
 * @author Renato Lui Geh
 */
public interface Filterable {
    
    /**
     * Filters the corresponding image to the given filter.
     * 
     * @param f The filter to be applied.
     */
    public void filter(Filter f);
}